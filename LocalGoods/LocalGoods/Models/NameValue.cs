	/// <summary>
	/// Collection of NameValueIntItem Items.
	/// </summary>
	public class NameValueIntCollection : List<NameValueIntItem>
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	_Indexer																															*
		//*-----------------------------------------------------------------------*
		/// <summary>
		/// Return an item from the collection by its name.
		/// </summary>
		/// <param name="name">
		/// Name of the item to find.
		/// </param>
		public NameValueIntItem this[string name]
		{
			get
			{
				NameValueIntItem ro = this.FirstOrDefault(x => x.Name == name);
				return ro;
			}
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	GetValue																															*
		//*-----------------------------------------------------------------------*
		/// <summary>
		/// Return the current value associated with the specified name.
		/// </summary>
		public int GetValue(string name)
		{
			NameValueIntItem item = this.FirstOrDefault(x => x.Name == name);
			int result = 0;

			if(item != null)
			{
				result = item.Value;
			}
			return result;
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	Increment																															*
		//*-----------------------------------------------------------------------*
		/// <summary>
		/// Increment the item with the specified name.
		/// </summary>
		public void Increment(string name)
		{
			NameValueIntItem item = this.FirstOrDefault(x => x.Name == name);

			if(item == null)
			{
				item = new NameValueIntItem();
				this.Add(item);
			}
			item.Value++;
		}
		//*-----------------------------------------------------------------------*



	}
	//*-------------------------------------------------------------------------*

	//*-------------------------------------------------------------------------*
	//*	NameValueIntItem																												*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// An name and int value pair.
	/// </summary>
	public class NameValueIntItem
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	Name																																	*
		//*-----------------------------------------------------------------------*
		private string mName = "";
		/// <summary>
		/// Get/Set the name of the item.
		/// </summary>
		public string Name
		{
			get { return mName; }
			set { mName = value; }
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	Value																																	*
		//*-----------------------------------------------------------------------*
		private int mValue = 0;
		/// <summary>
		/// Get/Set the value of the item.
		/// </summary>
		public int Value
		{
			get { return mValue; }
			set { mValue = value; }
		}
		//*-----------------------------------------------------------------------*

	}
